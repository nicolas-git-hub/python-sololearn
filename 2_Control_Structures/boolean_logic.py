# Python uses words instead of symbols like &&, || and !
# First booblean "and"

print(1 == 1 and 2 == 2)
print(1 == 1 and 2 == 3)
print(1 != 1 and 2 == 2)
print(2 < 1 and 3 > 6)

print("\n")
# Boolean "or"

print(1 == 1 or 2 == 2)
print(1 == 1 or 2 == 3)
print(1 != 1 or 2 == 2)
print(2 < 1 or 3 > 6)

print("\n")
print(not 1 == 1)
print(not 1 > 7)
