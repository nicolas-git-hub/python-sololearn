# Exponentiation

print("2**5 =")
print(2**5)
print("\n")

print("9**(1/2) =")
print(9**(1/2))

# Quotient & Remainder

print("Quotient => 20 // 6 =")
print(20 // 6)
print("\n")

print("Remainder => 1.25 % .5 =")
print(1.25 % .5)
print("\n")


# Result of a code
print("7 % (5 // 2) =")
print(7 % (5 // 2))
